import math
def CalculoCircunferencia(radio):
    pi = 3.141592
    return 2 * pi * radio

print ("La Circunferencia del radio 3: {:.6f}".format(CalculoCircunferencia(3)))
print ("La Circunferencia del radio 8: {:.6f}".format(CalculoCircunferencia(8)))
print ("La Circunferencia del radio 10: {:.6f}".format(CalculoCircunferencia(10)))